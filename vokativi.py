    dictionaries = [vokativi, vokativi_cirilica]
    lowercase_string = string.lower()
    
    for dictionary in dictionaries:
        for key, value in dictionary.items():
            if key.lower() == lowercase_string:
                return value.capitalize()
    
    # String is not a key in any of the dictionaries, return None
    return string

